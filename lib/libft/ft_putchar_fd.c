#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

/*
Description
The function putchar writes a character specified by the argument
on file descriptor.

Return Value
Outputs the character ’c’ to the given file
descriptor.
*/