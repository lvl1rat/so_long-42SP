#include "libft.h"

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}

// Ft_isascii checks if a given character (c) is present inside the ASCII table.
// - Returns 1 if true, 0 if false.