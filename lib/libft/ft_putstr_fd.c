#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}

/*
Description
The functio writes a string specified by the argument
on file descriptor.

Return Value
Outputs the string ’s’ to the given file
descriptor.
*/