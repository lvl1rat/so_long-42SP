#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}

/*
Description
This function will display the string s on the standard output
followed by a '\n', which is a new line.

Return Value
Outputs the string s to the standard output followed by a ’\n’.
*/