#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	return (i);
}

/*
Description 
The C library function strlen computes the length
of the string s up to, but not including the
terminating null character.

Return Value 
This function returns the length of string.
*/