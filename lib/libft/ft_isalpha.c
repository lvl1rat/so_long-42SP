#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}

// Ft_isalpha checkes if a given char (c) is alphabetic.
// - Returns 1 if true, 0 if false.
